import random

def generate_intro_message() -> str:
    greetings = [
        "Добрый день!",
        "Здравствуйте!",
        "Приветствую Вас!",
    ]

    introductions = [
        "Меня зовут Александр, я земельный эксперт проекта «Наследие».",
        "Я Александр, консультант проекта «Наследие».",
        "Меня зовут Александр, представляю проект «Наследие».",
        "Александр, проект «Наследие» — рад знакомству.",
        "Я Александр, специалист проекта «Наследие».",
    ]

    project_descriptions = [
        "Мы развиваем посёлки и предлагаем земельные участки в Крыму.",
        "Наша команда занимается развитием земельных участков на побережье Крыма.",
        "Мы помогаем выбрать и оформить участки в собственности на крымском побережье.",
        "Компания «Наследие» работает с земельными участками в Крыму — под отдых, строительство и инвестиции.",
        "Мы создаём благоустроенные посёлки в Крыму с удобной инфраструктурой.",
    ]

    questions = [
        "Подскажите, интересует ли Вас покупка земли у моря?",
        "Рассматриваете ли Вы покупку участка в Крыму?",
        "Интересна ли Вам тема приобретения земли у моря?",
        "Было бы Вам интересно узнать больше о покупке участков в Крыму?",
        "Хотели бы узнать немного подробнее о наших участках?",
    ]

    # Составляем сообщение из случайных элементов
    parts = [
        random.choice(greetings),
        random.choice(introductions),
        random.choice(project_descriptions),
        random.choice(questions),
    ]

    return " ".join(parts)
